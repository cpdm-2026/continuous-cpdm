# Evaluation naming conventions
# Endpoint FID mode expects:
#
#   gen_root/
#     cond1/
#       00000.png ...
#     cond2/
#       00000.png ...
#
# This mode is for baseline / endpoint-conditioned models.
#
#
# Sweep FID mode expects:
#
#   gen_root/
#     sz_p1_0/
#     sz_p0_8/
#     sz_p0_6/
#     ...
#     sz_0_0/
#     ...
#     sz_m1_0/
#
# This mode is for CPDM s_z sweep outputs generated by:
#
#   python scripts/sample.py sweep-save ...


# Endpoint-folder utilities
def find_endpoint_dirs(gen_root: str) -> Dict[str, str]:
    """Find endpoint folders for cond1/cond2 evaluation.

    Expected structure:
        gen_root/
          cond1/
          cond2/
    """
    root = Path(gen_root)

    if not root.exists():
        raise FileNotFoundError(f"endpoint gen_root does not exist: {gen_root}")

    if not root.is_dir():
        raise NotADirectoryError(f"endpoint gen_root is not a directory: {gen_root}")

    out = {}

    for name in ["cond1", "cond2"]:
        path = root / name
        if path.is_dir():
            out[name] = str(path)

    missing = [name for name in ["cond1", "cond2"] if name not in out]

    if missing:
        raise RuntimeError(
            f"Missing endpoint folders under {gen_root}: {missing}. "
            "Endpoint FID mode expects gen_root/cond1 and gen_root/cond2."
        )

    return out


# s_z sweep-folder utilities
def parse_sz_dirname(name: str) -> float:
    """Parse CPDM sweep directory names.

    Examples:
        sz_p1_0 -> +1.0
        sz_p0_8 -> +0.8
        sz_0_0  ->  0.0
        sz_m0_2 -> -0.2
        sz_m1_0 -> -1.0
    """
    name = os.path.basename(str(name).rstrip("/"))

    if name == "sz_0_0":
        return 0.0

    match = re.match(r"^sz_([pm])(\d+)_(\d+)$", name)

    if match is None:
        raise ValueError(f"Invalid s_z directory name: {name!r}")

    sign, integer_part, decimal_part = match.groups()
    value = float(f"{integer_part}.{decimal_part}")

    if sign == "m":
        value = -value

    return value


def find_sz_sweep_dirs(gen_root: str) -> List[Tuple[float, str, str]]:
    """Find s_z folders generated by save_cpdm_sweep_by_model_name.

    Expected structure:
        gen_root/
          sz_p1_0/
          sz_p0_8/
          ...
          sz_m1_0/

    Returns:
        List of (s_z, folder_name, folder_path), sorted from +1 to -1.
    """
    root = Path(gen_root)

    if not root.exists():
        raise FileNotFoundError(f"sweep gen_root does not exist: {gen_root}")

    if not root.is_dir():
        raise NotADirectoryError(f"sweep gen_root is not a directory: {gen_root}")

    out = []

    for path in root.iterdir():
        if not path.is_dir():
            continue

        try:
            s_z = parse_sz_dirname(path.name)
        except ValueError:
            continue

        out.append((s_z, path.name, str(path)))

    out.sort(key=lambda x: x[0], reverse=True)

    if not out:
        raise RuntimeError(
            f"No s_z folders found under {gen_root}. "
            "Sweep FID mode expects folders like "
            "sz_p1_0, sz_p0_8, sz_0_0, sz_m1_0."
        )

    return out


# Small row helpers
def _copy_if_exists(
    row: Dict[str, Any],
    source_key: str,
    target_key: str,
) -> None:
    if source_key in row:
        row[target_key] = row[source_key]


def _add_endpoint_target_fields(
    row: Dict[str, Any],
    domain_key: str,
    real_cond1_name: str,
    real_cond2_name: str,
) -> None:
    """Add target/cross FID fields for endpoint mode."""
    if domain_key == "cond1":
        row["target_real"] = real_cond1_name
        row["cross_real"] = real_cond2_name
        row["FID_target"] = float(row["FID_cond1"])
        row["FID_cross"] = float(row["FID_cond2"])

        _copy_if_exists(row, "KID_cond1_mean", "KID_target_mean")
        _copy_if_exists(row, "KID_cond1_std", "KID_target_std")
        _copy_if_exists(row, "KID_cond2_mean", "KID_cross_mean")
        _copy_if_exists(row, "KID_cond2_std", "KID_cross_std")

    elif domain_key == "cond2":
        row["target_real"] = real_cond2_name
        row["cross_real"] = real_cond1_name
        row["FID_target"] = float(row["FID_cond2"])
        row["FID_cross"] = float(row["FID_cond1"])

        _copy_if_exists(row, "KID_cond2_mean", "KID_target_mean")
        _copy_if_exists(row, "KID_cond2_std", "KID_target_std")
        _copy_if_exists(row, "KID_cond1_mean", "KID_cross_mean")
        _copy_if_exists(row, "KID_cond1_std", "KID_cross_std")

    else:
        raise ValueError(f"Unknown endpoint domain_key={domain_key!r}")


# Public mode 1: endpoint FID
def evaluate_endpoint_fid(
    gen_root: str,
    real_cache_cond1: str,
    real_cache_cond2: str,
    out_csv: str,
    out_json: Optional[str] = None,
    cond1_name: str = "cond1",
    cond2_name: str = "cond2",
    model_tag: Optional[str] = None,
    max_gen_images: int = 0,
    gen_sample_seed: int = 42,
    batch_size: int = 128,
    num_workers: int = 0,
    device: Optional[str] = None,
    compute_kid: bool = True,
    kid_subset_size: int = 1000,
    kid_n_subsets: int = 50,
    kid_seed: int = 123,
    export_subset: bool = False,
    export_root: Optional[str] = None,
) -> List[Dict[str, Any]]:
    """Evaluate endpoint/domain generated folders.

    Expected generated structure:
        gen_root/
          cond1/
          cond2/

    Intended use:
        - onehot
        - joint256
        - clip_img
        - clip_text
        - cond_quad_shift_ddpm
        - CPDM endpoint-only evaluation if saved as cond1/cond2

    Evaluation:
        cond1 folder is primarily compared with real_cache_cond1.
        cond2 folder is primarily compared with real_cache_cond2.
    """
    if model_tag is None:
        model_tag = os.path.basename(str(gen_root).rstrip("/"))

    real = load_two_real_caches(
        cond1_cache=real_cache_cond1,
        cond2_cache=real_cache_cond2,
        cond1_name=cond1_name,
        cond2_name=cond2_name,
    )

    bundle = build_inception_avgpool_extractor(device=device)
    endpoint_dirs = find_endpoint_dirs(gen_root)

    rows = []

    for domain_key in ["cond1", "cond2"]:
        gen_dir = endpoint_dirs[domain_key]

        export_dir = None
        if export_subset:
            export_dir = os.path.join(
                export_root or os.path.join(gen_root, "_eval_subsets"),
                domain_key,
            )

        print(f"[fid-endpoint] evaluating {domain_key}: {gen_dir}")

        row = evaluate_single_gen_dir(
            gen_dir=gen_dir,
            real=real,
            bundle=bundle,
            max_gen_images=max_gen_images,
            gen_sample_seed=gen_sample_seed,
            batch_size=batch_size,
            num_workers=num_workers,
            compute_kid=compute_kid,
            kid_subset_size=kid_subset_size,
            kid_n_subsets=kid_n_subsets,
            kid_seed=kid_seed,
            export_subset=export_subset,
            export_dir=export_dir,
        )

        row.update(
            {
                "mode": "endpoint",
                "model_tag": model_tag,
                "domain": domain_key,
                "real_cond1_name": real["cond1"].name,
                "real_cond2_name": real["cond2"].name,
                "real_cache_cond1": real["cond1"].path,
                "real_cache_cond2": real["cond2"].path,
            }
        )

        _add_endpoint_target_fields(
            row=row,
            domain_key=domain_key,
            real_cond1_name=real["cond1"].name,
            real_cond2_name=real["cond2"].name,
        )

        rows.append(row)

    save_rows_csv(rows, out_csv)

    if out_json is not None:
        save_rows_json(rows, out_json)

    return rows


# Public mode 2: CPDM s_z sweep FID
def evaluate_sweep_fid(
    gen_root: str,
    real_cache_cond1: str,
    real_cache_cond2: str,
    out_csv: str,
    out_json: Optional[str] = None,
    cond1_name: str = "flower",
    cond2_name: str = "leaf",
    model_tag: Optional[str] = None,
    max_gen_images: int = 0,
    gen_sample_seed: int = 42,
    batch_size: int = 128,
    num_workers: int = 0,
    device: Optional[str] = None,
    compute_kid: bool = True,
    kid_subset_size: int = 1000,
    kid_n_subsets: int = 50,
    kid_seed: int = 123,
    export_subset: bool = False,
    export_root: Optional[str] = None,
) -> List[Dict[str, Any]]:
    """Evaluate CPDM s_z sweep folders.

    Expected generated structure:
        gen_root/
          sz_p1_0/
          sz_p0_8/
          sz_p0_6/
          ...
          sz_0_0/
          ...
          sz_m1_0/

    Intended use:
        - base_cpdm
        - continuous_cpdm

    Each s_z folder is compared to both real endpoint caches.

    Important:
        The same gen_sample_seed is reused for every s_z folder.
        If max_gen_images > 0, this preserves the same sampled index subset
        across s_z folders when filenames are aligned.
    """
    if model_tag is None:
        model_tag = os.path.basename(str(gen_root).rstrip("/"))

    real = load_two_real_caches(
        cond1_cache=real_cache_cond1,
        cond2_cache=real_cache_cond2,
        cond1_name=cond1_name,
        cond2_name=cond2_name,
    )

    bundle = build_inception_avgpool_extractor(device=device)
    sweep_dirs = find_sz_sweep_dirs(gen_root)

    rows = []

    for s_z, folder_name, gen_dir in sweep_dirs:
        export_dir = None
        if export_subset:
            export_dir = os.path.join(
                export_root or os.path.join(gen_root, "_eval_subsets"),
                folder_name,
            )

        print(f"[fid-sweep] evaluating s_z={s_z:+.2f}: {gen_dir}")

        row = evaluate_single_gen_dir(
            gen_dir=gen_dir,
            real=real,
            bundle=bundle,
            max_gen_images=max_gen_images,
            gen_sample_seed=gen_sample_seed,
            batch_size=batch_size,
            num_workers=num_workers,
            compute_kid=compute_kid,
            kid_subset_size=kid_subset_size,
            kid_n_subsets=kid_n_subsets,
            kid_seed=kid_seed,
            export_subset=export_subset,
            export_dir=export_dir,
        )

        fid_cond1 = float(row["FID_cond1"])
        fid_cond2 = float(row["FID_cond2"])

        row.update(
            {
                "mode": "sweep",
                "model_tag": model_tag,
                "s_z": float(s_z),
                "folder_name": folder_name,
                "real_cond1_name": real["cond1"].name,
                "real_cond2_name": real["cond2"].name,
                "real_cache_cond1": real["cond1"].path,
                "real_cache_cond2": real["cond2"].path,
                "FID_gap_cond2_minus_cond1": float(fid_cond2 - fid_cond1),
            }
        )

        rows.append(row)

    rows = sorted(rows, key=lambda r: float(r["s_z"]), reverse=True)

    save_rows_csv(rows, out_csv)

    if out_json is not None:
        save_rows_json(rows, out_json)

    return rows